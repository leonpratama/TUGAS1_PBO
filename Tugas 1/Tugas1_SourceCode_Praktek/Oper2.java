public class Oper2 {
    public static void main(String[] args) {
        char i = 3, j = 4;

        System.out.println(i & j);
        System.out.println(i | j);
        System.out.println(i ^ j);
        System.out.println(Math.pow(i, j));
        System.out.println(~i);
    }
}