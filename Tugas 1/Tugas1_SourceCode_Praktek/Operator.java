public class Operator {
    public static void main(String[] args) {
        int i = 5, j = 2;

        System.out.println("Tambah = " + (i + j));
        System.out.println("Kurang = " + (i - j));
        System.out.println("Kali = " + (i * j));
        System.out.println("Bagi = " + (i / j));
        System.out.println("Modulus = " + (i % j));
    }
}